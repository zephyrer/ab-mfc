#include <io.h>
#include <cstdio>
#include <FCNTL.H>

void Cab_mfcView::OnConsole()
{
	if(!isConsole)
	{
		AllocConsole();
		freopen( "CONOUT$", "w+t", stdout );// 申请写
		freopen( "CONIN$", "r+t", stdin );// 申请读
	}
	else
	{
		FreeConsole();
	}
	isConsole = !isConsole;
}